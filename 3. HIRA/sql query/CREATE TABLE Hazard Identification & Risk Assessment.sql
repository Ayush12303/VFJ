CREATE TABLE hira (
    id INT,
    activity VARCHAR(255),
    hazard VARCHAR(255),
    risks VARCHAR(255),
    s INT,
    c INT,
    p INT,
    risk_rating INT,
    significant VARCHAR(255),
    action_plan VARCHAR(255),
    review_control VARCHAR(255),
    issue_number INT,
    issue_date DATE,
    revision_number INT,
    revision_date DATE
);
